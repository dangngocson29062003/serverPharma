const { default: mongoose } = require("mongoose");

const VoucherSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  code: {
    type: String,
    required: true,
    unique: true,
  },
  discountPercentage: {
    type: Number,
    required: true,
    min: 0,
    max: 100,
  },
  discountAmount: {
    type: Number,
    required: true,
    min: 0,
  },
  startDate: {
    type: Date,
    required: true,
  },
  endDate: {
    type: Date,
    required: true,
  },
  minOrderAmount: {
    type: Number,
    required: true,
    min: 0,
  },
  maxUsage: {
    type: Number,
    required: true,
    min: 1,
  },
  usageCount: {
    type: Number,
    default: 0,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

const VoucherModel = mongoose.model("vouchers", VoucherSchema);
module.exports = VoucherModel;
